console.log("---------------FOR LOOP WITH ARRAY-------")
var animals = ["oranges", "grapes", "potatos", "tomatos"];  // A list of fruits and vegetables
for (var i = 0; i < animals.length; i++) {                            // Loop through each animal in the list
    console.log("awesome " + animals[i] + ".", '\n');           // Print the current animal
}
console.log("---------------FOR LOOP-------")
for (var oranges = 50; oranges < 220; oranges++) {  // Start counting from 0, repeat until 20
    console.log("I have counted " + oranges + " oranges!");  // Print the current count of oranges
}
console.log("Zzzzzzzzzzz", '\n');                                     // Print the message

console.log("---------------WHILE LOOP-------")
var oranges = 50;         // Start counting from 0
while (oranges < 220) {  // Continue while oranges is less than 20
    console.log("I have found " + oranges + " oranges!");  // Print the current count
    oranges++;           // Increase the count by 1

}
