
// This function draws (prints) a cat face to the console multiple times
var drawCats = function (howManyTimes) {

    // Loop from 0 up to (but not including) howManyTimes
    for (var i = 0; i < howManyTimes; i++) {

        // Print the loop number and a cat face to the console
        console.log(i + " =^.^=");
    }
};

// Call the function and draw 10 cats
// You can change 10 to any number you like
drawCats(10);


for (var i = 8; i < 11; i++) {
    console.log(i + " =^.^=");
}

